                <h2 align="center">Названы темы программы  i-COMference 2012</h2>

                13-14 марта в пресс-центре РИА Новости (г.Москва, Зубовский бульвар
                д.4 ) пройдет конференция <a href="http://i-comference.ru/">i-СOMference</a>, посвященная коммуникациям и
                коммуникационным платформам в интернете: социальным сетям,
                интернет-СМИ и мобильным технологиям. Организатор конференции — журнал «Интернет в Цифрах», конференция проходит при экспертной поддержке Российской Ассоциации электронных коммуникаций (РАЭК) и РИА Новости.
                <br><br>

                Основной фокус этого года — коммуникации в мобильной и
                социальной среде. Мероприятие пройдет в три потока, каждый из
                которых имеет свой собственный формат:<br><br>

                - Дискуссионные панели об особенностях и ключевых моментах современных медиа, о тенденциях в развитии современных коммуникаций не только в бизнесе, но и в общественных процессах;<br><br>

                - Поток воркшопов, где эксперты сфокусируют своё внимание на кейсах и практических моментах коммуникационных проектов в новых медиа и мобильных решениях;<br><br>

                - В третьем потоке выступят эксперты с лекциями и ответят на
                вопросы в сфере коммуникаций и новых медиа.<br><br>

                Редакция журнала <a href="http://in-numbers.ru/">«Интернет в цифрах»</a> построила программу конференции
                таким образом, что слушатель, интересующийся отдельной темой, имеет
                возможность посетить секции всех трёх потоков, не боясь пропустить
                важное и интересное в других залах. Например,  теме мобильных
                технологий и коммуникаций посвящены дискуссионная панель, воркшоп и
                лекция, идущие друг за другом в разных залах.<br><br>

                Также в рамках <a href="http://i-comference.ru/">i-COMference</a> традиционно пройдёт ежегодная церемония
                награждения  лауреатов премии «Блог Рунета» — конкуса на выявление
                лучших российских блогов и блогеров.<br><br>

                Участие в конференции платное, стоимость участия составляет 3 000
                рублей при оплате в январе и феврале и 4 000 рублей при оплате в
                марте.<br><br>

                ВНИМАНИЕ! Количество мест ограничено, регистрацию и оплату участия
                необходимо осуществлять заранее!<br><br>

