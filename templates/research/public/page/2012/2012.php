  <h2 id="about">Oб исследовании</h2>
  <h3>История исследования</h3>
  <p>Проект исследования "Экономика Рунета" был инициирован в ходе апрельского <a href="http://2012.russianinternetforum.ru">РИФ+КИБ 2012</a> по инициативе Аналитического отдела <strong><a href="http://www.raec.ru">РАЭК</a></strong> и ряда интернет-компаний.</p> 
  <p>Традиционно - вот уже 16 лет - главная весенняя конференция Рунета открывается пленарным докладом, раскрывающим главные показатели развития отрасли. В 2012 году было решено пойти дальше - инициировав отдельное (ежегодное) исследование Экономики Рунета. </p>
  <p>Методика исследования разработана при участии <strong>Высшей Школой Экономики</strong>, основные векторы исследования согласованы с <strong>Boston Consulting Group (BCG)</strong>. </p>
  <p>Первоначальные глубинные интервью 78 экспертов были проведены в ходе РИФ+КИБ 2012.</p>
  <p>Дополнительно методика исследования и акценты исследования были обсуждены в ходе организованного РАЭК (в партнерстве с РВК и Руссофтом) IT-завтрака на <strong><a href="http://rocid.ru/events/forumspb12/">Петербургском Экономическом Форуме 2012</a></strong></p>
  <p>В течение периода май-июль 2012 года РАЭК и ВШЭ проводили на базе <strong><a href="http://www.rocid.ru">ROCID</a></strong> полевое исследование и массовый опрос экспертов.</p>
  <p>Результаты опроса - обрабатывались в течение сентября 2012 года и будут представлены <strong>на презентации 10 октября в РИА Новости.</strong></p>
  <h3>Цели и задачи исследования. Исследуемые рынки</h3>
  <p>Основная цель исследования – оценить текущее состояние тенденции развития российских рынков интернет-бизнеса, определить вклад интернет-экономики в экономику страны.</p>
  <p>Исследовательский проект «Экономика Рунета-2012» направлен на оценку объемов и состояния следующих основных российских интернет-рынков:</p>
  <ul style="padding-bottom: 20px;">
    <li><strong>Веб-разработка</strong></li>
    <li><strong>Контекстная реклама</strong></li>
    <li><strong>Медийная реклама</strong></li>
    <li><strong>Видеореклама</strong></li>
    <li><strong>SMM</strong></li>
    <li><strong>Поисковая оптимизация</strong></li>
    <li><strong>Коммерция - ритейл</strong></li>
    <li><strong>Коммерция - контент и игры</strong></li>
    <li><strong>Коммерция - платежи</strong></li>
    <li><strong>Хостинг и домены</strong></li>
    <li><strong>SaaS</strong></li>
  </ul>
  <p>По каждому из рынков выявляются <strong>следующие ключевые показатели</strong>:</p>
  <ul style="padding-bottom: 20px;">
    <li>Объемы рынка</li>
    <li>Прибыльность</li>
    <li>Динамика объема и прибыльности по сравнению с предыдущими годами</li>
    <li>Поквартальная динамика в течение года</li>
    <li>Структура рынка</li>
    <li>Прогноз динамики рынка в 2012 году</li>
  </ul>
  <p>Исследование состоит <strong>из двух основных частей</strong>:</p>
  <ul style="padding-bottom: 20px;">
    <li>Онлайн опрос экспертов-представителей игроков рынка. Анкета состоит из трех основных частей: информация об эксперте; финансовые данные по компании, которую представляет эксперт; финансовые оценки рынка в целом.</li>
    <li>Качественные (глубинные) интервью с экспертами, представляющими различные рынки.</li>
  </ul>
  
  <h2 id="partners">Партнеры</h2>
  <p>Инициаторы исследования: <strong><a href="http://www.raec.ru">РАЭК</a></strong> (Российская Ассоциация электронных коммуникаций) и <strong><a href="http://www.hse.ru">ВШЭ</a></strong> (Национальный исследовательский университет - Высшая школа экономики).</p>
  <p>Партнерами исследования выступают: <strong><a href="http://www.bcg.ru">BCG</a></strong> (Boston Consulting Group), <a href="http://www.google.ru/intl/ru/about/company/">Google</a>, <a href="http://corp.mail.ru">Mail.ru Group</a>, <a href="http://www.ozon.ru/context/detail/id/5085342/">OZON.ru</a>, <a href="http://www.fastlaneventures.ru/?page_id=1515">Fast Lane Ventures</a>.</p>
  
  <h2 id="experts">Эксперты</h2>
  <p>Экспертами исследования являются признанные отраслевые специалисты по 11 направлениям, совпадающим с изучаемыми рынками.</p>
  <p>Работа с экспертами велась как в ходе личных контактов исследователей, так и через систему <a href="http://www.rocid.ru">ROCID</a> - в рамках сформированных для исследования динамических экспертных групп. </p>
  <p>За период апрель-на сентябрь 2012 проведены следующие работы с экспертами:</p>
  <ul style="padding-bottom: 20px;">
    <li>Силами экспертов РАЭК выработаны подходы к сегментации интернет-рынка на <strong>11 суб-рынков</strong>, определены подходы к измерению экономик этих сегментов, определены списки экспертов по сегментам</li>
    <li>В ходе РИФ+КИБ 2012 проведено <strong>78 глубинных интервью</strong> (40-60 минут каждое) с представителями всех 11 исследуемых рынков, с привлечением преподавательского и студенческого стостава ВШЭ</li>
    <li>По итогам глубинных интервью силами экспертов ВШЭ подготовлены опросные анкеты для 2 типов экспертов: “гуру-эксперты” (по одному на каждый рынок) и “массовые эксперты” (минимум 10 на каждый рынок)</li>
    <li>Силами специалистов РАЭК осуществлено <strong>программирование опросных анкет</strong> обоих типов и подготовлены выборки экспертов из системы ROCID </li>
    <li>В течение мая проведена работа с <strong>"мега-экспертами"</strong></li>
    <li>В июне осуществлена переработка опросных анкет по итогам предложений и критических замечаний “мега-экспертов”</li>
    <li>В период июль-август проводились массовые опросы экспертов в виде <strong>полевых исследований и опросов</strong>, в результате которых собрано более 300 анкет.</li>
    <li>В сентябре производилась обработка результатов массовых опросов, обсуждение итогов с “мега-экспертами” и партнерами исследования.</li>
    <li>В октябре 2012 исследование будет презентовано и обсуждено с привлечением экспертов в ходе специальных мероприятий, в т.ч. в рамках Недели Российского Интернета <a href="http://www.riw12.com">RIW-2012</a>. </li>
  </ul>
