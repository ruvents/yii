  <h2 id="results">Результаты исследования. Итоговый отчет</h2>
  <p>Основными итогами исследования станут:</p>
  <ul style="padding-bottom: 20px;">
    <li style="color: red;">Суммарный объем интернет-экономики и ее вклад в ВВП России (включая <strong>процент интернет-экономики от ВВП</strong>)</li>
    <li>Точки роста интернет-эономики</li>
    <li>Болевые точки и сдерживающие факторы</li>
    <li>Рекомендации для участников интернет-рынка, государства и профильных ассоциаций по дальнейшему стимулированию развития интернет-экономики</li>
  </ul>
  
  <p><strong>Результаты исследования</strong> будут представлены в виде самостоятельного продукта и презентованы интернет-игрокам, широкой общественности, СМИ, государству <strong>10 октября 2012 года в 11:00 в ходе специальной презентации в РИА Новостях</strong>.</p>
  <p>Результаты исследования и возможная модификация методики будут обсуждаться <strong>17-19 октября в Экспоцентре - в ходе Недели Российского Интернета - <a href="http://www.riw12.com">RIW-2012</a></strong>.</p>
  <p>Впоследствии исследование будет проводиться <strong>ежегодно</strong>.</p>
  
  <h3>Итоговый аналитический отчет</h3>
  <p>Итоги исследования будут систематизированы, обработаны и представлены в виде самостоятельного аналитического отчета.</p>
  <p>Отчет будет подготовлен в трех видах:</p>
  <ul style="padding-bottom: 20px;">
    <li><strong>В печатном виде</strong> (брошюра с инфографикой, интервью, таблицами и графиками): лайт-версия (готовится к 10 октября 2012 года) и полноценная версия (конец 2012 года).</li>
    <li><strong>В формате электронной презентации</strong>, содержащей основные итоги исследования</li>
    <li><strong>В электронном виде</strong> (размещение электронной версии на специальном интернет-ресурсе): PDF-версия + “листаемая” презентация с итогами исследования.</li>
  </ul>
  <p><strong>Результаты исследования</strong> будут представлены в виде самостоятельного продукта и презентованы интернет-игрокам, широкой общественности, СМИ, государству.</p>
  
