  <h3 id="about">O мероприятии</h3>
  <p>13 марта 2013 года в г. Москве в конференц-центре Digital October (Берсеневская наб., 6) прошла Международная конференция по обеспечению детской безопасности и цифровой грамотности в сети Интернет. </p>
  <p>Организаторами выступили <b><a href="http://www.minsvyaz.ru" target="_blank" >Минкомсвязь России</a></b> и <b><a href="http://www.raec.ru" target="_blank" >РАЭК</a></b>, при поддержке <b><a href="http://googlerussiablog.blogspot.ru/" target="_blank" >Google Россия</a></b>.</p>
  <p>Главными темамич конференции стали: российский и международный подходы к обеспечению безопасности в сети Интернет и цифровому образованию, включая развитие законодательства, саморегулирование интернет-отрасли и программы интегрированного обучения детей и взрослых навыкам безопасного использования интернета.</p>
  <p>Участникам конференции были представлены результаты первого всероссийского исследования Фонда развития Интернет и Левада-центра, проведенного при поддержке компании Google Россия и посвященного опыту использования Интернета и потребностям в обучении цифровой грамотности российских школьников и их учителей и родителей (см. раздел &quot;<a href="http://rocid.ru/childsafety2013/#materials">Материалы</a>&quot;). Также в ходе конференции прошла презентация новой комплексной образовательно-методической программы по обучению школьников в возрасте от 13 до 15 лет навыкам коммуникации и безопасности в Интернете и апробация в виде открытого урока со школьниками.</p>
  <p>В конференции приняли участие более 200 человек, представителей федеральных и региональных органов государственной власти, интернет-индустрии и экспертного сообщества, СМИ. </p>
  <p>А в специализированных образовательных секциях конференции также приняли участие педагоги, специалисты из учреждений сферы образования и социальной защиты, ученики средних школ.</p>
  <p>Важным акцентом конференции стало активное участие в ней экспертом международного уровня: <b><a href="http://rocid.ru/142786/" target="_blank" >Deborah McGovern</a></b> - Заместитель директора Internet <b>Watch Foundation</b> и <b><a href="http://rocid.ru/142787/" target="_blank" >Andrea Parola</a></b> - Координатор коалиции ЕС &ldquo;Безопасный Интернет детям&rdquo;. </p>
  <p>По итогам конференции <a target="_blank" href="http://lenta.ru/">Lenta.ru</a> взяла расширенное интервью у Andrea Parola: <a target="_blank" href="http://lenta.ru/articles/2013/03/14/children/">http://lenta.ru/articles/2013/03/14/children/</a></p>
  
