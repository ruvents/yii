                <h2>Эксперты</h2>

                <p>Экспертами исследования являются признанные отраслевые специалисты по 11 направлениям, совпадающим с изучаемыми рынками.</p>

                <p>Работа с экспертами велась как в ходе личных контактов исследователей, так и через систему
                    <a href="http://www.runet-id.com">RUNET-ID</a> - в рамках сформированных для исследования динамических экспертных групп.
                </p>

                <p>За период апрель-на сентябрь 2012 проведены следующие работы с экспертами:</p>
                <ul style="padding-bottom: 20px;">
                    <li>Силами экспертов РАЭК выработаны подходы к сегментации интернет-рынка на
                        <strong>11 суб-рынков</strong>, определены подходы к измерению экономик этих сегментов, определены списки экспертов по сегментам
                    </li>
                    <li>В ходе РИФ+КИБ 2012 проведено
                        <strong>78 глубинных интервью</strong> (40-60 минут каждое) с представителями всех 11 исследуемых рынков, с привлечением преподавательского и студенческого стостава ВШЭ
                    </li>
                    <li>По итогам глубинных интервью силами экспертов ВШЭ подготовлены опросные анкеты для 2 типов экспертов: “гуру-эксперты” (по одному на каждый рынок) и “массовые эксперты” (минимум 10 на каждый рынок)</li>
                    <li>Силами специалистов РАЭК осуществлено
                        <strong>программирование опросных анкет</strong> обоих типов и подготовлены выборки экспертов из системы ROCID
                    </li>
                    <li>В течение мая проведена работа с <strong>"мега-экспертами"</strong></li>
                    <li>В июне осуществлена переработка опросных анкет по итогам предложений и критических замечаний “мега-экспертов”</li>
                    <li>В период июль-август проводились массовые опросы экспертов в виде
                        <strong>полевых исследований и опросов</strong>, в результате которых собрано более 300 анкет.
                    </li>
                    <li>В сентябре производилась обработка результатов массовых опросов, обсуждение итогов с “мега-экспертами” и партнерами исследования.</li>
                    <li>В октябре 2012 исследование будет презентовано и обсуждено с привлечением экспертов в ходе специальных мероприятий, в т.ч. в рамках Недели Российского Интернета
                        <a href="http://www.riw12.com">RIW-2012</a>.
                    </li>
                </ul>

