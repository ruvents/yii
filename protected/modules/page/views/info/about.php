        	<li>агрегатор новостей Рунета (самое важное из того, что произошло за день - в партнерстве с theRunet.com)</li>
        	<li>самые «вкусные» вакансии медиа- и интернет-индустрии</li>
        	<li>скидки на партнерские мероприятия + дисконт в интернет-магазинах</li>
        </ul>
