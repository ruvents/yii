<p><strong>Накануне старта РИФ+КИБ в нашем гипермаркете конференций Runet-ID.com обновление ассортимента :)
Теперь через привычный Вам интерфейс Вы можете приобрести билеты на целую серию технологических конференций, ближайшая из которых - профессиональная конференция веб-разработчиков "Российские интернет-технологии" (http://www.ritconf.ru/), которая пройдёт в Москве 22 и 23 апреля. Тематика конференции охватывает все аспекты создания
