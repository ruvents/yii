Евгений Гордеев (Pluso.ru) расскажет свою историю успеха о том, как за год Pluso трансформировалось из сервиса социальных кнопок в крупнейший провайдер информации о пользователях Рунета со своими рекламодателями и аналитической системой.

Дмитрий Навоша, руководитель ведущего в России спортивного сайта Sports.ru поведает об эволюции медийных стартапов и о роли технологий в успехе медиа.

Сергей Котырев (UMI) представил две темы и Программный комитет до сих пор бьётся в муках выбора:

- Бизнес на коробочном софте против бизнеса на облачном софте: чем выгоднее заниматься и где;
- Процессно- и результативно-ориентированные сотрудники: как управлять и в чем польза?

Юрий Устинов, исполнительный директор Русоникс, прочитает доклад об успешном репозиционировании компании после 11 лет работы, компании с многолетним укладом и вагонами обязательств перед клиентами.

Руководители РИА Новостей (Сергей Волокитин и Анатолий Стояновский, CPO и CTO ;) раскроют тему управления крупной технологической компанией – от проектного офиса для упорядочивания корпоративного проектного хаоса и до построения системы взаимоотношений с аутсорсинговыми компаниями.

Максим Вишнивецкий (DIO-soft) рассмотрит практические аспекты эффективной работы с бюрократией, а Николай Зайченко (бюро Александра Невского) расскажет слушателям о том, как грамотно оформить и защитить бренд в онлайне и как правильно оформить трудовые отношения с творческими сотрудниками, не забыв при этом и про оформление интеллектуальной собственности.

Андрей Рыжкин (Agima) расскажет о том, как зарабатывать на разработке (то есть как построить действительно рентабельную разработку), а Алексей Рыбак (Badoo) – о эволюции разработки крупного интернет-проекта.

