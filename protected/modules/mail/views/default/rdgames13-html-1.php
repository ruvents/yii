<p>Добрый день!</p>

<p>Мы рады сообщить, что открыта регистрация и подача работ на <b>Первые Всероссийские Digital-Игры:</b> <a href="http://russiandigitalgames.ru">www.russiandigitalgames.ru</a>. Мы приглашаем всех пользователей Runet-ID принять участие в конкурсе!</p>

<p>В отличие от стандартных фестивалей, на которые участники подают уже готовые работы из портфолио, в рамках Игр претендентам предстоит разработать конкурсные работы по единым заданиям в одной из шести номинаций. Среди призов конкурса – статуэтки и дипломы победителей Игр, гаджеты и гран-при от Ростелекома в размере 300 000 рублей! И, конечно, слава, признание рынка и множество новых клиентов. Игры идут как в личном, так и в корпоративном зачетах, так что призов хватит всем.</p>

<p>Проект организован РАЭК (Российской Ассоциацией Электронных Коммуникаций) и проходит в рамках десятой юбилейной Премии Рунета. Работы по номинациям будут приниматься до 14 ноября, а церемония награждения пройдет 21 ноября в рамках вечерней программы Премии Рунета!</p>

<p>Игры проходят в шести номинациях, и уже сейчас вы можете приступить к работе над заданиями:</p>
