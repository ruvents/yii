                    <h2 style="text-align: center;">Продолжается прием заявок на присуждение ежегодной премии SaaS-решений "Облака 2013"</h2>

                    <p><b>Для участия в конкурсе, на сайте премии необходимо <a href="http://2013.cloudaward.ru/#nominate">заполнить форму</a> номинанта до 1 октября 2013 года. Все заявки в обязательном порядке будут рассмотрены организационным комитетом премии. </b></p>
                    <p><b>С 1 по 13 октября пройдет Экспертное и Народное голосование.</b></p>
                    <p>Итоги «Облаков 2013» будут подведены 18 октября 2013 года в Москве в конференц-центре гостиницы Novotel Moscow City.</p>
                    <p>Премия «Облака 2013» вручается в следующих номинациях:</p>
                    <ul>
                      <li>Облака для Бизнеса – награда вручается за разработку и развитие лучших решений, реализуемых в b2b-сфере;</li>
                      <li>Облака для Пользователей - номинация отмечает простоту, удобство и наиболее комфортное существования в облаке конечного пользователя;</li>
                      <li>Облака и Наука – в данной номинации отмечаются научные работы, изыскания и исследования компаний, вносящие значительный вклад в область SaaS;</li>
                      <li>Мобильное облако – фактически, каждое мобильное приложение так или иначе использует «облачные» решения. Награда выделяет лучшие разработки и наиболее оригинальные решения в данной области;</li>
                      <li>Облака для Государства - награда вручается за реализацию лучшего решения на государственном уровне;</li>
                      <li>Облачный Стартап Года – отмечается команда/проект/компания, наиболее ярко проявившие себя в области SaaS-решений в 2013 году.</li>
                    </ul>
                    <p>Номинации вне общего конкурса:</p>
                    <ul>
                      <li>Облака и СМИ – приз вручается представителям прессы за популяризацию SaaS-рынка и демонстрацию
                        достижений и задач, стоящих перед данной отраслью;
                      </li>
                      <li>Гран-При Премии - специальный блок наград от Оргкомитета, спонсоров и партнеров проекта.</li>
                    </ul>
