                    <h3>Уважаемые коллеги!</h3>

                    <p>Российская Ассоциация Электронных Коммуникаций (РАЭК) и Деловая среда, объявляют о начале приема заявок на присуждение ежегодной премии  SaaS-решений "Облака 2013" - отраслевой награды, отмечающей достижения в области развития и популяризации SaaS-технологий в России, создания прогрессивного  SaaS-сообщества в Рунете и демонстрации отечественных наработок и  достижений в сфере «облачных технологий».</p>

                    <p>Конкурс проводится по инициативе РАЭК и призван привлечь  широкий интерес к «облачным технологиям»,  как со стороны представителей бизнеса и органов государственной власти, так и со стороны простых пользователей.</p>

                    <p>В рамках конкурса пройдет отбор лучших онлайн сервисов и компаний, реализующих проекты в сфере «облачных технологий». Премия присуждается в нескольких  номинациях, и каждая номинация подразумевает 3-х лауреатов-победителей: по итогам Экспертного голосования и  Народного голосования, за лучшее внедрение SaaS решения награждается компания, использующая облачные технологии. Также, по решению организационного комитета будут отмечены журналисты, пишущие на тему «облаков».</p>

