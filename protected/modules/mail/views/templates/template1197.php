<h3>Уважаемые участники конференции &ldquo;Поколение NEXT&rdquo;!</h3>

<p>До начала конференции &ldquo;Поколение NEXT&rdquo; осталось совсем немного времени.</p>

<p>Ждем вас <strong>3-го ноября 2016 года</strong> в Павильоне 8.1 Центрального выставочного комплекса &laquo;Экспоцентр&raquo; (Краснопресненская наб., 14). Конференция проходит в рамках Russian Interactive Week (RIW 2016). Убедительно просим вас приезжать строго к времени, обозначенном в предыдущем письме.</p>

<p><strong>ВАЖНАЯ ИНФОРМАЦИЯ ПО ПРОХОДУ НА МЕРОПРИЯТИЕ</strong></p>

<p><strong>3 ноября 2016 </strong>вход на конференцию &ldquo;Поколение NEXT&rdquo; будет осуществляться только с <strong>Южного входа</strong> (со стороны набережной). Проход с Западного входа будет запрещен в связи с проведением другого мероприятия на территории комплекса.</p>

