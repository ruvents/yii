<h3>Кто научит банки продавать в онлайне?</h3>

<p>Банки плохо продают в интернете. Это факт. Миллионы пользователей проходят по страницам банковских сайтов, совершают одну-две простейшие транзакции и исчезают в неизвестности. Сайты банков оторваны от их же интернет-банков и наоборот. Кто может научить банки продавать в онлайне?</p>

<p>Безусловно — те, кто умеет это делать хорошо и делает это каждый день: онлайн-ритейлеры, онлайн-трэвел сервисы и другие онлайн-бизнесы. <strong>Особенностью <a href="http://fininternet.ru/?r_source=runet_id_email">Форума FinInternet–2014</a> станет панельная дискуссия «Продажи в интернете. Учимся у лучших»</strong>, <a href="http://fininternet.ru/?s=programm&r_source=runet_id_email">участвуют</a> Островок, Евросеть, Wikimart и Biletix и другие.</p>


