<p>17&nbsp;декабря 2014 года пройдет ежегодная конференция <a href="http://runet-id.com/event/itogi2014/">&laquo;РУНЕТ 2014: ИТОГИ ГОДА&raquo;</a>.</p>

<p>В&nbsp;ходе конференции ведущие эксперты российской интернет-отрасли, представители профильных государственных структур и&nbsp;общественных организаций представят итоговые показатели развития российского интернета и&nbsp;ИКТ в&nbsp;2014&nbsp;году, обсудят современные тренды развития отрасли высоких технологий и&nbsp;сделают прогнозы развития отрасли на&nbsp;2015&nbsp;год.</p>

<h3>Конференция состоит из&nbsp;трех блоков:</h3>

<ol>
	<li><strong>Открытие</strong> <nobr>(11:30&ndash;12:00)</nobr></li>
	<li><strong>Ток-шоу &laquo;Рунет 2014: Итоги года&raquo;</strong> <nobr>(12:00&ndash;13:00)</nobr></li>
	<li><strong>Закрытый круглый стол &laquo;Новые технологии на&nbsp;</strong><strong>службе бизнеса&raquo;</strong> <nobr>(13:30&ndash;15:00)</nobr></li>
</ol>

<p>Организаторами конференции выступают РАЭК (<a href="http://www.raec.ru/">www.raec.ru</a>) и&nbsp;ФРИИ (<a href="http://www.iidf.ru/">www.iidf.ru</a>), организационную, экспертную и&nbsp;информационную поддержку&nbsp;&mdash; оказывают ведущие игроки интернет-отрасли, профильные гос. учреждения (Госдума, Минкомсвязь, Роскомнадзор), институты развития, профильные ассоциации, представители СМИ.</p>

<p>К&nbsp;участию в&nbsp;конференции &laquo;Рунет 2014: Итоги года&raquo; приглашаются представители игроков всех отраслей высоких технологий (интернет, телеком, медиа, софт и&nbsp;IT), эксперты отрасли, представители органов государственной власти, представители СМИ.</p>

<p>Участие в&nbsp;конференции&nbsp;&mdash; бесплатное для всех зарегистрированных участников. После регистрации необходимо получить подтверждение от&nbsp;организаторов конференции.</p>

