	<li><span style="line-height: 1.6em;">Стратегии операторов связи &ndash; истории успеха: от оптимизации затрат до программ лояльности</span></li>
</ul>

<p><span style="line-height: 1.6em;">Своим опытом, стратегиями и видением рынка поделятся: генеральный директор «Мегафон» Иван Таврин, президент ТТК Артем Кудрявцев, генеральный директор МТТ Евгений Васильев, генеральный директор «ЭР-Телеком Холдинг» Андрей Семериков, исполнительный директор Инновационного центра ОАО «Ростелеком» Павел Ройтберг, директор по маркетингу и развитию продуктов МГТС Дмитрий Кулаковский, генеральный директор интернет-кинотеатра ivi.ru Олег Туманов, генеральный директор SPB TV Кирилл Филиппов и многие другие.</p>

<p><span style="line-height: 1.6em;">Участниками форума станут представители компаний операторов мобильной, фиксированной и спутниковой связи, производителей телекоммуникационного оборудования, операторов платного телевидения, системных интеграторов, производителей контента, а также представители государственных органов власти и отраслевых СМИ.</span></p>

