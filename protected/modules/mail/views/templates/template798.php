<h3 dir="ltr">О проекте:</h3>
Форум пройдет в Международный День безопасного Интернета (Safer Internet Day) &ndash; главную мировую дату, посвященную цифровой безопасности и позитивному Интернету. День безопасного Интернета отмечается с 2004 года по инициативе сети Центров безопасного Интернета Insafe и в этом году затронет 120 стран. Являясь официальным мероприятием Дня безопасного Интернета для России, Форум станет кульминацией Недели безопасного Рунета &ndash; официальной российской серии мероприятий по цифровой безопасности, проходящих во многих регионах нашей страны с 2 по 9 февраля.

