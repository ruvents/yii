<p dir="ltr" id="docs-internal-guid-cd99caef-63f7-0835-a2c3-3e8d940fc63f">9 февраля 2016 года в Москве на площадке Digital October пройдет ведущее российское мероприятие по безопасности в сфере цифровых технологий &ndash;<br />
<strong>Cyber Security Forum / i-SAFETY 2016</strong> &ndash; <a href="http://www.cybersecurityforum.ru">www.CyberSecurityForum.ru</a></p>

<p dir="ltr"><a href="http://runet-id.com/event/csf16/">Программа</a> форума объединяет круглые столы и секции, посвященные различным аспектам цифровой безопасности &ndash; кибербезопасность предприятий и организаций малого и крупного бизнеса, безопасность экономических отношений в сети, защита от контентных угроз, цифровая грамотность пользователей и цифровой суверенитет.</p>

<p dir="ltr">Задача Форума &ndash; это обмен опытом (технологии, законодательство, решения), выявление лучших практик в сфере информационной безопасности, межотраслевой диалог и диалог &ldquo;Общество &ndash; Бизнес &ndash; Государство&rdquo;.</p>

<p dir="ltr">Форум станет первой площадкой, где состоится публичное обсуждение первых результатов работы закона <strong>&ldquo;О праве на забвение&rdquo;</strong> &ndash; будет представлена статистика запросов и дан анализ, как пользователи понимают новые возможности своей защиты.</p>

<p dir="ltr">Помимо этого, эксперты отрасли, представители госорганов и общественность обсудят вопросы<strong> оборота и защиты персональных данных</strong> пользователей в сети и формирование безопасного и позитивного пространства в Рунете (сервисы и контент).</p>

<p dir="ltr">Традиционно специальное внимание будет уделено <strong>цифровой безопасности</strong> детей и подростков, а также использованию интернет-технологий в их этическом воспитании и образовании. Особая тема Форума &ndash; <strong>цифровая грамотность</strong> пользователей Рунета, методика ее измерения и способы ее повышения.</p>

<p dir="ltr">Организатором &nbsp;и партнерами Форума выступают: РОЦИТ, РАЭК, РСпектр, Российско-Британская торговая палата. Поддержка Форума: Минкомсвязь, Роскомнадзор.</p>

