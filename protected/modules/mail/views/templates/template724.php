					<p style="font-size: 14px; font-weight: normal; margin: 0 0 10px; padding: 0;">Пятница 23 октября — стала третьим, заключительным днем RIW 2015 — Российской Интерактивной Недели. Главное мероприятие осени и отрасли высоких технологий подошло к концу, а мы уже готовы подвести предварительные краткие итоги этого трёхдневного интеллектуального марафона.</p>
