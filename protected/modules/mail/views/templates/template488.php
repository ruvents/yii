<p>21&nbsp;мая 2015 года в&nbsp;Челябинске, в&nbsp;конференц-центре гостиницы Radisson Blu (ул. Труда, 179), ведущие представители крупнейших российских IT-компаний впервые проведут конференцию &laquo;РИФ.Челябинск&raquo; в&nbsp;рамках большого всероссийского проекта &laquo;РИФ.Регион 2015&raquo;.</p>

<p><a href="http://region.rif.ru/" target="_blank">РИФ.Регион</a>&nbsp;&mdash; это большой всероссийский проект про интернет, коммуникации, IT-бизнес, стартапы и&nbsp;новые технологии. Это также своеобразное региональное продолжение федерального РИФоКИБа с&nbsp;участием федеральных и&nbsp;региональных спикеров, единый формат и&nbsp;единая концепция организации проведения, контент-наполнения мероприятий, профильные секции, круглые столы и&nbsp;мастер-классы от&nbsp;гуру интернета..</p>

<p>Организаторами и&nbsp;партнерами проекта выступают: РАЭК, ФРИИ, Yota, РОЦИТ, Минкомсвязь России. Поддержку проекту оказывают российские интернет-компании, федеральные и&nbsp;региональные игроки IT-рынка.</p>

