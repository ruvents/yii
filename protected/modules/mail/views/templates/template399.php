<h3>О чем этот курс?</h3>

<p>С ростом инвестиций в Интернет требования рекламодателей к эффективности рекламы возрастают. Для большинства брендов становится приоритетной не только имиджевая реклама, но и кампании, способствующие продажам и росту бизнеса. Возможности таргетинга в интернете приобретают особое значение для большинства рекламодателей: развиваются технологии закупок рекламы, инструменты анализа рекламных проектов.</p>

<p>Performance маркетинговые кампании способны приносить лучшую отдачу от рекламных инвестиций, а значит современному маркетинг-менеджеру крайне важно разбираться в инструментах performance маркетинга, его настройках и метриках.
</p>

