<p>Конференция “Рунет 2015: ИТОГИ ГОДА” – традиционное декабрьское мероприятие РАЭК и партнеров Ассоциации, подводящее интернет-итоги уходящего года. В этом году за счет расширенного участия партнеров, главными темами конференции станут:</p>
<ul>
	<li><strong>Экономика Рунета:</strong><br />
	всем участникам будет презентован расширенный отчет и брошюра &ldquo;Экономика Рунета&rdquo; с главными показателями развития отрасли, подготовленная в сотрудничестве РАЭК, ВШЭ, ФРИИ, Google, Kaspersky Lab, Microsoft, OZON.ru, Finex, Спутник (основные показатели развития отрасли и прогноз &ndash; в рамках исследования <a href="http://xn--80aaokjbmheeb2a2al4l.xn--p1ai/2015/">&ldquo;Экономика Рунета 2014&ndash;2015&rdquo;</a>)</li>
	<li>
	<p><strong>Регулирование Рунета:</strong><br />
	будет представлен ежегодных РАЭКовский &ldquo;Правовой дайджест&rdquo;, демонстрирующий все тренды регулирования ИКТ в России и дающий главные выводы о возможной смене парадигмы с охранительно-запретительной на стимулирующую</p>
