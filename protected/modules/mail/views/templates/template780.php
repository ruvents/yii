<p><b>Со спикерами и подробной программой можно ознакомится  <a href="http://runet-id.com/event/itogi2015/#event_widgets_ProgramGrid ">на сайте конференции</a></b></p>

<p>Акцентом конференции этого года стала оценка влияния кризиса на дальнейшее развитие индустрии и корректировка предыдущих прогнозов развития. В этой части исследования был проведен опрос экспертов относительно кризисных явлений в экономике, и как меняются настроения в индустрии относительно различных факторов — так называемый <b>Кризисный барометр рынка</b> -  новый индекс, сформированный и впервые измеренный в 2015 году экспертами РАЭК и НИУ ВШЭ.</p>

<p>Мероприятие посвящено актуальным вопросам отрасли и станет не просто своеобразным подведением итогов, но и разработкой примерного плана действий на следующий год.</p>

