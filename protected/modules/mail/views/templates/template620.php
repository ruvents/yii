<p><b>Компании Rusbase и Global Innovation Labs приглашают 18 сентября на международную конференцию про большие данные ICBDA 2015</b>. На одной площадке соберутся создатели новых технологий в области Big Data и представители различных отраслей бизнеса, а также научные сотрудники и аспиранты. </p>

<p>Для пользоваелей RUNET-ID действует специальная цена на участие в конференции — скидка 10% по промо-коду <b>RUNET-ID</b>.</p>

<hr style="border: none; border-top: 1px solid #eaeaea; height: 1px; margin: 25px 0;" />

<p><b>Основные направления программы:</b></p>
<ul>
<li style="padding-bottom:5px;"><b>Big Data для финансового и страхового сектора.</b><br/>
Эксперты расскажут, как с помощью данных и аналитики повысить эффективность продаж в банке, как предсказать следующее действие клиента и влияние на его финансовое поведение.</li>

<li style="padding-bottom:5px;"><b>Big Data в ритейле.</b><br/>
Участники конференции узнают, как  с помощью онлайн и оффлайн данных стимулировать потребление в период рецессии, как нализ данных может увеличить прибыль розничных сетей.</li>

<li style="padding-bottom:5px;"><b>Big Data в рекламе.</b><br/>
На секции будут обсуждать технологические решения и сервисы по работе с большими данными, которые успешно внедрены в рекламной индустрии и показывают хорошие результаты</li>

<li style="padding-bottom:5px;"><b>Семантический анализ данных и машинное обучение.</b><br/>
Слушатели секции узнают про нейронные сетки, использование машинного обучения на больших данных на примере задачи классификации интернет-пользователей, а также про особенности сбора, обработки и использования данных.</li>

<li><b>Анализ и классификация данных.</b><br/>
Эксперты расскажут про технологии кластеризация сайтов при сборе и анализе пользовательских данных, о том, как хранить миллионы файлов сейчас, и миллиард файлов в будущем.</li>
</ul>
<p>Среди спикеров конференции представители компаний Тинькофф Банк, Data-Centric Alliance, Acronis, Яндекс, TNS Россия, MailRu Group, 1C и многие другие. Более подробная информация и программа доступны на сайте <a hrefg="http://icbda2015.org/">http://icbda2015.org/</a><p>.

