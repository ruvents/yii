<p><strong>Легальный контент. Итоги голосования экспертов ИРИ.</strong></p>

<p>Итоги голосования экспертов Института Развития Интернета по теме текущего состояния рынка легального контента в России представили на форуме &laquo;Интернет+Медиа&raquo;. Участие в опросе ИРИ приняли 263 эксперта&ndash; представители ведущих российских интернет-компаний.</p>

<p>&laquo;Результаты указывают на то, что провайдерам легального контента следует активизировать усилия для того, чтобы сделать свои сервисы более популярными, удобными и конкурентосопособными по сравнению с пиратскими ресурсами, чтобы победить в борьбе за потребителя&raquo;, <strong>- <em>сказал директор РАЭК, член Совета ИРИ Сергей Плуготаренко.</em></strong></p>

