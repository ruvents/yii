<p>10 мая 2016 года начал свою работу Форум &laquo;Интернет+Медиа&raquo; - первый из восьми тематических форумов, которые Институт Развития Интернета (ИРИ) проводит в этом году для работы над дорожными картами интернет-индустрии и поручениями Президента РФ Владимира Путина по итогам форума &laquo;Интернет Экономика 2015&raquo;.</p>

<p>Форум собрал на своей площадке представителей государственности власти, топ-менеджеров и экспертов медиа и телекома, которые обсуждают регулирование интернета, медиа, телекоммуникаций, актуальные тенденции развития и новые инициативы отрасли.</p>

<hr />
<p><strong>Открытие Форума</strong></p>

<p>Старт работе Форума дал Сергей Петров, который анонсировал его ключевые темы: недискриминационный доступ операторов в жилые дома, подходы в правовом регулировании ОТТ-сервисов, реформа системы управления авторскими правами, борьба с пиратством и популяризация легального контента.</p>

<p>&laquo;Рунет сегодня &ndash; это и есть медиа, охватывающее более 80 млн человек, <strong><em>- отметил Советник Президента РФ, Председатель Совета ИРИ Герман Клименко. </em></strong>-Это огромная экосистема со множеством игроков, интересы которых в чем-то совпадают, в чем-то противоречат, дополняют друг друга и переплетаются. Форум &quot;Интернет+ Медиа&quot; станет важным этапом в поиске общих решений, которые позволят ускорить развитие Рунета&raquo;.</p>

