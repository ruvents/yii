												<p><em>12 сентября в рамках первого дня </em><em>Международной конференции по новым образовательным технологиям</em><em> #EdCrunch, для участников будет открыта увлекательная экскурсия в Кидзанию, всемирно известный парк игрового обучения.</em></p>

												<p>Кидзания - это социально-значимый международный проект, в основе которого лежит принцип игрового обучения.</p>

												<p>12 сентября с 16:00 до 18:00 мини-город целиком будет работать только для взрослых. Правительство Кидзании предложит участникам конференции не просто познакомиться с проектом, но и попробовать самостоятельно ответить на вопрос, есть ли связь между парком и образовательной программой.</p>

												<p>Участники посетят экскурсии, в рамках которых познакомятся с философией и легендой проекта, а затем смогут попробовать себя в десятках различных профессий, представленных в парке: выйти на сцену театра, научиться лечить кариес, поработать на почте, отреставрировать произведения искусства, потушить пожар и многое другое. В этот день взрослые смогут войти в игровые зоны и сами выполнить те задания, которые потенциально будут выполнять их ученики, посмотреть примеры уроков, послушать рекомендации специалистов.</p>

