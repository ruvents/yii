                      <p class="caption blue" style="line-height:22px;color:#2BABE3;font-weight:bold;font-size:20px;">Направления работы CSF 2017</p>
                      <p style="line-height:22px;">Основная задача Форума&nbsp;&mdash; межотраслевой диалог и&nbsp;обмен опытом в&nbsp;области информационной безопасности, выявление лучших практик по&nbsp;направлениям: технологии, решения, законодательство, цифровая грамотность.</p>
                      <p style="line-height:22px;">Секции Форума посвящены <nobr>программно-техническим</nobr>, обучающим, юридическим вопросам безопасности в&nbsp;цифровой среде, проблемам законодательства и&nbsp;принятия решений, роли специалистов и&nbsp;пользователей в&nbsp;информационной, мобильной и&nbsp;кибербезопасности, формированию позитивной <nobr>интернет-среды</nobr> и&nbsp;позитивного контента.</p>
                      <p style="line-height:22px;">В&nbsp;работе CSF 2017 примут участие ведущие специалисты по&nbsp;кибербезопасности из&nbsp;<nobr>интернет-индустрии</nobr> и&nbsp;смежных отраслей, представители профильных государственных и&nbsp;правоохранительных органов, технические специалисты, эксперты в&nbsp;области позитивного контента, представители исследовательского и&nbsp;образовательного сообщества.</p>
                      <p style="line-height:22px;">Cyber Security Forum&nbsp;&mdash; это отличная возможность полностью погрузиться в&nbsp;тему кибербезопасности, перенять опыт экспертов в&nbsp;этой области, обменяться мнениями с&nbsp;коллегами, познакомиться с&nbsp;гуру отрасли.</p>
