	<li>Что будет способствовать дальнейшему росту интернета?</li>
	<li>Трансграничность интернета &ndash; это благо или проблема?</li>
</ul>

<p>Среди докладчиков форума &ndash; известные российские и зарубежные интернет-эксперты, представители государственных и общественных организаций, руководители крупнейших интернет-компаний - <strong>Крис Бакридж</strong> (RIPE NCC), <strong>Людмила Бокова</strong> (Совет Федерации РФ), <strong>Елена Воронина</strong> (MSK-IX), <strong>Олег Демидов</strong> (ПИР-Центр), <strong>Фредерик Донк</strong> (ISOC), <strong>Уильям Дрейк</strong> (Университет Цюриха), <strong>Вольфанг Кляйнвахтер</strong> (Университет Аархуса), <strong>Леонид Левин</strong> (Государственная Дума РФ), <strong>Дмитрий Мариничев</strong> (RadiusGroup), <strong>Маарит Паловирта</strong> (ISOC), <strong>Леонид Тодоров</strong> (APTLD), <strong>Люнь Хань</strong> (CNNIC), <strong>Роберт Шлегель</strong> (Государственная Дума РФ), <strong>Михаил Якушев</strong> (ICANN) и многие другие.</p>

<p>RIGF-2016 состоится 7 апреля 2016 г. в Москве, в Лотте-Отеле. Мероприятие проводится Координационным центром национального домена сети Интернет при поддержке ICANN, RIPE NCC, Лаборатория Касперского, APTLD, РОЦИТ, ИРИ и РАЭК.</p>

