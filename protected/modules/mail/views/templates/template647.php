<p><b>21 сентября 2015 года в 11.00 в «ФРИИ Сити холл» состоится пресс-конференция, "Поколение NEXT "Школа новых технологий"</b>, посвященная вопросам безопасного использования интернета детьми и анонсу большой конференции в рамках RIW 2015. Организаторы: Фонд «Разумный Интернет» при поддержке РОЦИТ и РАЭК.</p>

<p>В программе мероприятия: проекты по цифровой грамотности, достижения и наработки специалистов в сфере IT и педагогов, круглый стол «Интернет и Дети», панельная дискуссия.</p>

