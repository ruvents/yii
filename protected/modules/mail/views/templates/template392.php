<p>Координационный центр национального домена сети Интернет приглашает на <strong>Шестой российский форум по управлению интернетом (RIGF-2015)</strong>, который состоится <strong>7 апреля 2015 года</strong> в Лотте-отеле (Москва). </p>

<p>На форуме будут обсуждаться наиболее актуальные темы современного состояния сети.  В программе форума – обсуждение вопросов поддержания сетевого нейтралитета, юрисдикций и налогообложения интернет-бизнеса и торговли в национальных сегментах различных стран. Участники и докладчики займутся выявлением точек возможных технологических прорывов и тенденций развития интернета как технологической платформы. Делегаты форума из стран БРИКС расскажут о том, какие подходы к проблеме управления интернетом и передовые практики  в этой области применяются в этих государствах. </p>


<p>Кроме того, в форуме принимают участие представители практически всех национальных  кириллических доменов, что позволяет со всех строн обсдуить перспективы развития «кириллической вселенной» и связанные с этим вызовы и проблемы.</p>


