<p>Традиционную лекцию в рамках RIGF в этом году прочтет Яри Аркко - специалист по архитектуре интернета в Ericsson Research (Финляндия), директор Общей зоны и председатель Инженерного совета интернета (IETF). Он расскажет участникам форума о ключевых технологиях, а также принципах и нововведениях, которые лежат в основе внедрения "интернета вещей".</p>

