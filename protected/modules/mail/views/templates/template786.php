<h2>Акценты Форума</h2>
<p>В&nbsp;первый день Форума&nbsp;— 21&nbsp;декабря&nbsp;— пройдёт 8&nbsp;круглых столов, в&nbsp;ходе которых игроки интернет-отрасли обсудят с&nbsp;участием представителей профильных министерств и&nbsp;ведомств и&nbsp;ведущих представителей бизнеса перспективы развития тех областей экономики страны, где внедрение интернетизированных систем и&nbsp;процессов может дать максимальный эффект. Каждый круглый стол соберёт экспертов по&nbsp;направлениям и&nbsp;темам: Интернет + ОБЩЕСТВО, Интернет + ОБРАЗОВАНИЕ, Интернет + ТОРГОВЛЯ, Интернет + ГОРОД, Интернет + МЕДИЦИНА, Интернет + ФИНАНСЫ, Интернет + МЕДИА, ИТ + Суверенитет. </p>

