<p>Главными и&nbsp;связующими темами Форума станут инициативы и&nbsp;предложения <strong>Института Развития Интернета (ИРИ)</strong> по&nbsp;усилению интернетизации различных областей экономики России, разработанные в&nbsp;рамках Поручений <strong>Президента РФ&nbsp;Владимира Путина</strong> от&nbsp;19&nbsp;мая 2015&nbsp;года.</p>
<p>К&nbsp;участию в&nbsp;работе Форума «Интернет Экономика 2015» приглашены руководители крупнейших отраслевых организаций, министерств и&nbsp;ведомств, а&nbsp;также значимых российских IT-компаний: Минобрнауки, Рособрнадзор, Минкомсвязь, Минздрав, Минэкономразвития, ФНС, ФТС, Госдума, Роскомнадзор, Минстрой, Минпромторг, Яндекс, Лаборатория Касперского, Mail.ru Group, Rambler&amp;Co, РБК, SoftKey, 1С, Ростелеком и&nbsp;т.д. </p>
<p>Важным концептуальным завершением Форума станет большое Пленарное заседание 22&nbsp;декабря, которое соберёт руководителей и&nbsp;представителей всех профильных организаций и&nbsp;учреждений бизнеса и&nbsp;власти, заинтересованных в&nbsp;развитии Интернет Экономики.</p>

