                        <p>7 апреля 2017 года в Иннополисе (Республика Татарстан) пройдет Восьмой Российский форум по управлению интернетом (<a href="http://rigf.ru/">RIGF-2017</a>). В этом году форум посвящен вопросам вовлечения в процесс глобального управления интернетом новых участников и изменения текущей повестки, которая требует обсуждения все более широкого круга смежных вопросов. <strong>Приглашаем вас принять участие в форуме!</strong></p>

<p>На сайте RIGF 2017 опубликована <a href="http://rigf.ru/prog/">программа мероприятия</a>. Во время форума пройдут секции, посвященные вопросам кибербезопасности, технологических инноваций, теме электронного государства, а также различиям в европейском и азиатском подходе к развитию интернета. Представители разных стран обсудят, кто должен принимать более активное участие в глобальном управлении интернетом и как можно привлечь новых стейкхолдеров.</p>

<p>Среди докладчиков RIGF 2017 &ndash; известные российские и зарубежные эксперты: Али Альмешал (ISOC Бахрейн), Роберто Гаэтано (ISOC), Олег Демидов (ПИР-Центр), Андрей Колесников (Ассоциация интернета вещей), Михаил Якушев (ICANN), Дмитрий Мариничев (RadiusGroup), Нинг Конг (CNNIC), Маарит Паловирта (ISOC), Сергей Повалишев (Hoster.by), Шавкат Сабиров (IT-ассоциация Казахстана), Алексей Семеняка (RIPE NCC), Тран Мин Тан (VNNIC), Андрей Ярных (Лаборатория Касперского) и многие другие.</p>

<p>Особенное внимание уделяется привлечению к управлению интернетом молодежи, поэтому в рамках форума исполнительный директор APTLD Леонид Тодоров прочитает специальную лекцию об основах управления интернетом для студентов и молодых специалистов из Татарстана. Также во время форума пройдет награждение победителей конкурса IP&amp;IT Law для студентов юридических специальностей, изучающих право в интернете и информационных технологиях.</p>

<p>Форум организован Координационным центром национального домена сети Интернет при поддержке Министерства связи и массовых коммуникаций РФ, Министерства информатизации и связи Республики Татарстан, ICANN, ISOC, RIPE NCC и APTLD. Партнер форума по кибербезопасности &ndash; Лаборатория Касперского.</p>
                        
                        
