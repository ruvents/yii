<p><strong>24 февраля в Москве пройдет конференция </strong><strong>ConversionConf2015, посвященная повышению конверсии и эффективности онлайн продаж.&nbsp; </strong></p>

<p>Высокие позиции в поисковых рейтингах автоматически не приводят к росту числа звонков, продаж и конверсий. Как добиться, чтобы максимальное количество посетителей сайта сделало то, что от них ждут: подписались, позвонили, купили? На этот вопрос развернуто ответят спикеры узкоспециализированной конференции ConversionConf2015.</p>

<p><strong>Что такое </strong><strong>ConversionConf</strong><strong>?</strong></p>

<p>Конференция ConversionConf&nbsp; проходит с 2013 года в двух столицах: Москве и Санкт-Петербурге. Мероприятие собирает интернет-маркетологов, владельцев компаний, аналитиков, специалистов по продвижению и всех заинтересованных в росте эффективности сайта.</p>

<p><strong>Программа</strong></p>

<p>Программа конференции разбита на тематические секции, каждая из которых посвящена этапам воронки продаж: стратегии, трафик, конверсия, допродажи.</p>

<p><strong>Стратегии:</strong> прогнозирование, распределение бюджета и выбор способов достижения KPI в интернет-продажах.</p>

<p><strong>Трафик:</strong> новейшие методики привлечения клиентов из социальных и СРА-сетей, контекстной и медийной рекламы, а также из других источников.</p>

<p><strong>Конверсии:</strong>&nbsp;лайфхаки по увеличению продаж с сайта.</p>

<p><strong>Допродажи:</strong> эффективное увеличение объемов продаж с текущим количеством посетителей сайта.</p>

