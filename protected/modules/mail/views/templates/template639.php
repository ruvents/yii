<p><b>24 сентября, в рамках форума «Спектр-2015» пройдет Российский Интернет Форум - <a href="https://regions.rif.ru/events/sochi/">РИФ.Сочи</a>, который станет важным этапом большого всероссийского проекта РИФ.Регион 2015.</b>.</p>


