<p>С презентацией о состоянии экономики Рунета выступит директор Российской Ассоциации Электронных Коммуникаций (РАЭК) <b>Сергей Плуготаренко</b>.</p>
<p>Также секции будут посвящены таким темам как:</p>
<ul>
<li style="padding-bottom:5px;">Инфраструктура Рунета и преодоление цифрового разрыва;</li>
<li style="padding-bottom:5px;">Интернет-бизнес и развитие Экосистем Рунета;</li>
<li style="padding-bottom:5px;">Электронная коммерция, Реклама, Платные сервисы, Веб-разработка, Мобильные технологии, Digital Marketing, Безопастность в Сети;</li>
<li style="padding-bottom:5px;">Инвестиции, стартапы и интернет-предпринимательство;</li>
<li style="padding-bottom:5px;">Образование и кадры для IT-отрасли и т.д.</li>
