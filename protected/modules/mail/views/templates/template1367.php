                      <p style="line-height:22px;margin:0;padding:15px 0 0 0;"><b>С&nbsp;24 по&nbsp;26&nbsp;мая 2017 года</b> в&nbsp;Иннополисе (республика Татарстан) пройдут конференции &laquo;Цифровая индустрия промышленной <nobr>России-2017</nobr>&raquo; и&nbsp;РИФ.Иннополис, часть большого регионального проекта &laquo;РИФ+КИБ&raquo; РИФ.Регион. Посетители Иннополиса в&nbsp;эти дни смогут, находясь на&nbsp;одной площадке, посещать секции ЦИПР и&nbsp;РИФ.</p>
                      <p style="line-height:22px;margin:0;padding:15px 0 0 0;"><a href="http://www.cipr.ru/" target="_blank" class="blue-a" style="color:#0a3fa7;font-weight:bold;text-transform:uppercase;font-size:16px;">Конференция ЦИПР</a>&nbsp;&mdash; это актуальная межотраслевая площадка для глобального диалога представителей промышленности, профессионалов отрасли информационных технологий, телекома, оборонного комплекса, венчурных инвесторов и&nbsp;государства по&nbsp;вопросам развития цифровой экономики, несырьевого экспорта и&nbsp;обеспечения кибербезопасности.</p>
                      <p style="line-height:22px;margin:0;padding:15px 0 0 0;"><a href="http://innopolis.rif.ru/" target="_blank" class="blue-a" style="color:#0a3fa7;font-weight:bold;text-transform:uppercase;font-size:16px;">РИФ.Иннополис</a>&nbsp;&mdash; это часть крупнейшего всероссийского проекта РИФ.Регион, посвященного Интернету, экономике, коммуникациям, <nobr>интернет-бизнесу</nobr> и&nbsp;новым технологиям, продолжение исторического Форума &laquo;РИФ+КИБ&raquo;, ежегодно проходящего в&nbsp;Подмосковье уже более 20 лет подряд.</p>
                      <p style="line-height:22px;margin:0;padding:15px 0 0 0;">В&nbsp;рамках ЦИПР будет проведено <b>более 20 круглых столов</b>, посвященных вопросам развития информационной и&nbsp;кибербезопасности, интернета вещей, а&nbsp;также новым инструментам организации управления экономическими и&nbsp;производственными процессами. Решения, принятые в&nbsp;рамках этих дискуссий, лягут в&nbsp;основу принимаемых руководством страны решений по&nbsp;поддержке высокотехнологичных отраслей экономики.</p>
                      <p style="line-height:22px;margin:0;padding:15px 0 0 0;">Среди гостей <nobr>ЦИПР-2017</nobr> ожидаются <b>советники Президента</b> Андрей Белоусов и&nbsp;Игорь Щеголев, <b>Министр связи и&nbsp;массовых коммуникаций</b> Николай Никифоров, <b>Министр промышленности и&nbsp;торговли</b> Денис Мантуров, <b>Министр экономического развития</b> Максим Орешкин, <b>глава госкорпорации Ростех</b> Сергей Чемезов и&nbsp;<b>Президент Республики Татарстан</b> Рустам Минниханов.</p>
