<h3><strong>16 декабря во ФРИИ Сити Холле (Серебряническая набережная, д. 29) состоится ежегодная конференция &laquo;Рунет 2015: Итоги Года&raquo;.</strong></h3>

<p>В первой части пройдет открывающая панель на тему &ldquo;Рунет 2015: Итоги года&rdquo;, а также главными в рамках конференции станут показатели по итогам года Экономики Рунета за 2014-2015 года с новыми дополнениями, выводами и прогнозами. Первые результаты, презентованные в октябре опубликованы на сайте <a href="http://xn--80aaokjbmheeb2a2al4l.xn--p1ai/">ЭкономикаРунета.рф</a>.</p>

<p>Акцентом исследования этого года стала оценка влияния кризиса на дальнейшее развитие индустрии и корректировка предыдущих прогнозов развития. В этой части исследования был проведен опрос экспертов относительно кризисных явлений в экономике, и как меняются настроения в индустрии относительно различных факторов &mdash; так называемый Барометр рынка - &nbsp;новый термин, описанный экспертами РАЭК и НИУ ВШЭ.</p>

