<p style="text-align: center;"><strong>ПРИГЛАШАЕМ ПОСЕТИТЬ КОНФЕРЕНЦИЮ<br/> «DIGITAL OUT-OF-HOME И DIGITAL SIGNAGE В РИТЕЙЛЕ»<br/>

Современные цифровые технологии для маркетинга розничных сетей</strong><br/>

2 ноября 2016, Москва, Экспоцентр, павильон «Форум»</p>


<p>Конференция <a href="http://isrussia.ru/visitors/events/bussines/konferentsii/konferentsiya-digital-out-of-home-i-digital-signage-v-riteyle/">"Digital out-of-Home и Digital Signage в ритейле"</a> даст возможность поделиться опытом использования технологий Digital-out-of-Home и Digital Signage в розничных сетях, а также обсудить тенденции развития этого рынка в России.</p>

<p>Конференция соберет вместе <strong>специалистов</strong> индустрии DooH / Digital Signage, Digital агентства, операторов наружной рекламы, владельцев и операторов медиафасадов, производителей LED экранов, а также <strong>заказчиков</strong> - девелоперов и управляющих торгово-развлекательными центров, представителей розничных сетей и сетевых магазинов, ресторанов и кафе и многих других.</p>

