<p><strong>Темы для обсуждения: </strong></p>

<ul>
	<li>Рынок DooH в России и за рубежом. Основные тренды развития</li>
	<li>Инновационные подходы к планированию рекламных кампаний в сегменте цифровой DooH-рекламы</li>
	<li>Интерактивные решения в современном торговом центре</li>
	<li>Использование digital-технологий в ТЦ как эффективного инструмента маркетинга</li>
	<li>Новые форматы наружной, транзитной и indoor рекламы</li>
</ul>

