<h2 style="font-family: 'Helvetica Neue', Helvetica, Arial, 'Lucida Grande', sans-serif; font-size: 28px; line-height: 1.2; color: #000; font-weight: 200; margin: 40px 0 10px; padding: 0;">Hello <?=$user->getShortName()?>,</h2>

<p>We remind you that the 8th Russian Internet Governance Forum will take place on April 7th, 2017 at the University of Innopolis.</p>

<h3 style="font-family: 'Helvetica Neue', Helvetica, Arial, 'Lucida Grande', sans-serif; font-size: 20px; line-height: 1.2; color: #000; font-weight: 200; margin: 40px 0 10px; padding: 0;"><span style="color:#A2386D">Agenda</span></h3>

<p>Official opening of the Forum is at <strong>11.00</strong>.</p>

<p>Several main topics will be discussed at the Forum: cybersecurity, e-government, international cooperation in the field of Internet governance and the development of innovations. <a href="http://rigf.ru/en/prog/">Forum agenda</a>.</p>

<h3 style="font-family: 'Helvetica Neue', Helvetica, Arial, 'Lucida Grande', sans-serif; font-size: 20px; line-height: 1.2; color: #000; font-weight: 200; margin: 40px 0 10px; padding: 0;"><span style="color:#A2386D">Introductory lecture</span></h3>
<p>In 2017, the Forum will focus on attracting new actors, first of all, students and young people, to the issues of Internet governance.</p>

<p>An introductory lecture on Internet governance will be organized for beginners. The audience will learn what are the main principles of Internet governance, what approaches exist in different regions of the world, what are the main issues and challenges that the global Internet community faces. The speaker is Leonid Todorov, General Manager at the Asia Pacific Top Level Domain Association. The lecture starts at <strong>9.30</strong>.</p>

<h3 style="font-family: 'Helvetica Neue', Helvetica, Arial, 'Lucida Grande', sans-serif; font-size: 20px; line-height: 1.2; color: #000; font-weight: 200; margin: 40px 0 10px; padding: 0;"><span style="color:#A2386D">How to get there</span></h3>

<p>You can reach University of Innopolis by using a shuttle bus, which will run on April 7th between Innopolis and “Relita” Hotel in Kazan (85G, Dekabristov St., Kazan). Travel time is at least 40 minutes (depending on the traffic). <a href="http://rigf.ru/en/particip/?p=how">Bus timetable</a>.</p>

