<p>Напоминаем вам, что 8-й Российский форум по управлению интернетом пройдет 7 апреля 2017 в Университете Иннополис.</p>

<h3 style="font-family: 'Helvetica Neue', Helvetica, Arial, 'Lucida Grande', sans-serif; font-size: 20px; line-height: 1.2; color: #000; font-weight: 200; margin: 40px 0 10px; padding: 0;"><span style="color:#A2386D">Программа</span></h3>

<p>Официальное открытие форума в <strong>11:00</strong>.</p>

<p>В ходе форума пройдут обсуждения по нескольким основным темам: &nbsp;кибербезопасность, электронное правительство, международное сотрудничество в области управления интернетом и развитие инноваций. <a href="http://rigf.ru/prog/">Программа форума</a>.</p>

<h3 style="font-family: 'Helvetica Neue', Helvetica, Arial, 'Lucida Grande', sans-serif; font-size: 20px; line-height: 1.2; color: #000; font-weight: 200; margin: 40px 0 10px; padding: 0;"><span style="color:#A2386D">Вводная лекция</span></h3>

<p>В 2017 году на форуме сделан акцент на привлечение новых участников к вопросам управления интернетом, и в первую очередь студентов и молодежи.</p>

<p>В программе форума предусмотрена вводная лекция по управлению интернетом для начинающих, из которой слушатели смогут узнать основные принципы управления интернетом, о том, какие подходы к этому существуют в разных регионах мира, какие главные проблемы и вызовы стоят перед мировым интернет-сообществом. Лекцию прочтет генеральный менеджер Ассоциации регистратур национальных доменов верхнего уровня Азиатско-Тихоокеанского региона Леонид Тодоров. Начало выступления в <strong>9:30</strong>.</p>

<h3 style="font-family: 'Helvetica Neue', Helvetica, Arial, 'Lucida Grande', sans-serif; font-size: 20px; line-height: 1.2; color: #000; font-weight: 200; margin: 40px 0 10px; padding: 0;"><span style="color:#A2386D">Как добраться</span></h3>

<p>Добраться до Университета Иннополис можно на автобусе-шатле, который будет курсировать 7 апреля между Иннополисом и отелем «Релита» в Казани (ул. Декабристов, д. 85Г). Время в пути не менее 40 минут (в зависимости от пробок). <a href="http://rigf.ru/particip/?p=how">Расписание автобусов</a>.</p>

