<p>Приглашаем вас посетить <strong>Восьмой российский форум по управлению интернетом (</strong><a href="http://rigf.ru/"><strong>RIGF-2017</strong></a><strong>). RIGF-2017 состоится 7 апреля 2017 г. в университете города Иннополис, Республика Татарстан</strong>. Мероприятие проводится Координационным центром национального домена сети Интернет при поддержке Министерства информатизации и связи Республики Татарстан, ICANN, RIPE NCC, Лаборатории Касперского, APTLD и ISOC.</p>

<p>В <a href="http://rigf.ru/prog/?p=prog">программе мероприятия</a> будут обсуждаться следующие вопросы:</p>

<ul>
	<li>Современные интернет-угрозы и тенденции в области кибербезопасности.</li>
	<li>Специфика управления интернетом на евразийском пространстве</li>
	<li>Особенности построения электронного правительства в различных странах и регионах</li>
	<li>Инновации и новые рубежи развития интернета.</li>
</ul>

<p>В этом году Форум будет посвящен вопросам вовлечения в процесс глобального управления интернетом новых участников и изменения текущей повестки, которая требует обсуждения все более широкого круга смежных вопросов. Среди докладчиков Форума &ndash; известные российские и зарубежные интернет-эксперты, представители государственных и общественных организаций, руководители крупнейших интернет-компаний &ndash; Томас Шнайдер (ICANN), Олег Демидов (ПИР-Центр), Дмитрий Мариничев (RadiusGroup), Маарит Паловирта (ISOC), Леонид Тодоров (APTLD), Андрей Ярных (Лаборатория Касперского), Патрик Пенникс (Совет Европы), Михаил Якушев (ICANN) и многие другие.</p>



