<h2>Программа и Выставка СПИК 2014</h2>

<p>В этом году программа конференции будет включать в себя все главные темы развития интернета, а акцент будет сделан на темах: интернет-маркетинг и реклама, веб-аналитика, SEO, веб-разработка и разработка мобильных приложений, электронная коммерция и т.д.</p>

<p>Отдельным блоком пройдут мастер-классы по специализированным тематикам, где специалисты поделятся опытом создания, развития и продвижения бизнеса в интернете.</p>

