<h2>О конференции</h2>

<p>Ежегодно мероприятие собирает более 2500 участников: это лидеры интернет-бизнеса, эксперты и специалисты по инфокоммуникациям и интернет-технологиям, сотрудники российских и западных IT-компаний, генеральные директора и владельцы интернет-бизнесов, маркетологи, PR-специалисты, копирайтеры, юзабилисты, представители СМИ, госучреждений, а также рядовые интернет-пользователи.</p>

<p>Традиционно СПИК привлекает весомую часть профессиональной аудитории Северо-Запада России, а также представителей различных отраслей бизнеса, которым интересен интернет-аспект развития их компаний, в том числе представителей офлайнового бизнеса, госсектора, заказчиков и рекламодателей.</p>

<p>Организаторы мероприятия: РАЭК (Ассоциация элекронных коммуникаций) и группа компаний TRINET.</p>

