<p><strong>На открытии конференции выступят:</strong></p>

<ul>
	<li><strong>Людмила Бокова</strong>, член Совета Федерации Федерального Собрания Российской Федерации, расскажет о практике и итогах проведения Единого урока безопасного интернета;</li>
	<li><strong>Сергей Гребенников</strong>, директор РОЦИТ, представит данные исследования особенностей поведения детей и их родителей в интернете за 2016 г.</li>
	<li><strong>Антонина Приезжева</strong>, заместитель руководителя Роскомнадзора, расскажет о специфике платформы отраслевого взаимодействия в области защиты личной информации граждан &ldquo;Цифровой дом&rdquo;</li>
	<li><strong>Игорь Марчак</strong>, руководитель проектного офиса &laquo;Школа новых технологий&raquo;, расскажет о тенденциях развития школы будущего</li>
	<li><strong>Татьяна Мартьянова</strong>, начальник управления по связям с общественностью МГТС, поднимет тему доступности интернета в школах;</li>
	<li><strong>Екатерина Рудых</strong>, директор ИРИ, выступит с темой &ldquo;Интернет+Образование&rdquo;.</li>
</ul>

