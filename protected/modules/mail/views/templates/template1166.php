<p>3 ноября в рамках Russian Interactive Week 2016 &nbsp;состоится конференция <a href="http://riw.moscow/special/next">&ldquo;Поколение NEXT&rdquo;</a>, посвященная вопросам безопасного использования интернета детьми. Организаторы: РОЦИТ, РАЭК, проект &ldquo;Школа новых технологий&rdquo;. Генеральные партнеры проекта - МГТС и DEPO Computers.</p>

<p>&ldquo;Поколение NEXT&rdquo; - это практико-ориентированная площадка, объединяющая детей, педагогов, а также интернет-индустрию и представителей органов власти для развития школы будущего. Для детей и педагогов готовится специальная образовательная программа, посвященная навыкам безопасного пользования интернетом и миру IT-профессий, а также рассказ о смелых идеях, свежих проектах и уникальных практиках в образовании в формате PechaKucha.</p>

<p>Главное отличие программы этого года в разделении мероприятий для детской и взрослой аудитории. Для педагогов &ndash; это возможность обменяться опытом с коллегами &laquo;по цеху&raquo;, обучиться новым навыкам, представить достижения образовательного учреждения в профессиональном сообществе.</p>

